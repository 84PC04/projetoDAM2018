    private void preencherComboBoxAlunos(){
        try {
            //trazendo todos os alunos do BD
            Aluno a = new Aluno();
            DadosAluno dados = new DadosAluno();
            this.listaAlunos = dados.listar(a);
            //carregar os alunos no combobox
            DefaultComboBoxModel modelo = new DefaultComboBoxModel();
            for (Aluno al : listaAlunos) {
                modelo.addElement(al.getNome());
            }
            jComboBoxAlunos.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

