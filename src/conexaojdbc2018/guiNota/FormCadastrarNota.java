    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        // TODO add your handling code here:
        try {
            DadosNota dados = new DadosNota();
            Nota n = new Nota();
            n.setNota(Float.parseFloat(jTextFieldNota.getText()));
            n.setProva(jTextFieldTipoProva.getText());
            int index = jComboBoxAlunos.getSelectedIndex();
            Aluno a = this.listaAlunos.get(index);
            n.setAluno(a);
            dados.cadastrarNota(n);
            JOptionPane.showMessageDialog(null, "Nota cadastrada");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

